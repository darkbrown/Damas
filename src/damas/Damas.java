    public Queue<JButton> bloquearTablero(JButton [][] tablero, Queue<JButton> cola){
        for(int i=0; i<8; i++){
            for(int j=0; j<8; j++){
               
               if (!"O".equals(tablero[i][j].getText())){
               
                     tablero[i][j].setOpaque(rootPaneCheckingEnabled);
                     tablero[i][j].setEnabled(false);
                                    
               }
               cola.add(tablero[i][j]);
               
            }
        }
        return cola;
    }
    
